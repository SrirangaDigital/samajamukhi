			<h3>ಮೋದಿಕೇರ್</h3>
			<p>ಕೇಂದ್ರ ಸರ್ಕಾರ ತನ್ನ ಅತ್ಯಂತ ಮಹತ್ವಾಕಾಂಕ್ಷಿ ಯೋಜನೆಯಾಗಿ ಈ ಬಾರಿಯ ಮುಂಗಡ ಪತ್ರದಲ್ಲಿ ದೇಶದ 50 ಕೋಟಿ ಬಡಜನರ ಆರೋಗ್ಯ ಸುರಕ್ಷೆಗಾಗಿ ‘ರಾಷ್ಟ್ರೀಯ ಆರೋಗ್ಯ ಸುರಕ್ಷಾ ಯೋಜನೆ’ಯನ್ನು ಘೋಷಿಸಿದೆ. ವಿಶ್ವದ ಅತ್ಯಂತ ದೊಡ್ಡಗಾತ್ರದ ಈ ಆರೋಗ್ಯ ಕವಚದಲ್ಲಿ ದೇಶದ 10 ಕೋಟಿ ಕುಟುಂಬಗಳಿಗೆ ವಾರ್ಷಿಕ 5 ಲಕ್ಷ ರೂಗಳ ಆರೋಗ್ಯ ವಿಮೆ ನೀಡಲಾಗುವುದು. ಈ ಚಿಕಿತ್ಸಾ ವೆಚ್ಚದಲ್ಲಿ ರೋಗಿಗಳ ಖಾಯಿಲೆಗಳ ಎಲ್ಲಾ ಹಂತಗಳ ಖರ್ಚು ಭರಿಸುವ ವ್ಯವಸ್ಥೆಯಿದೆ. ಜೊತೆಗೆ ದೇಶದ ಎಲ್ಲಾ ಲೋಕಸಭಾ ಕ್ಷೇತ್ರಗಳಲ್ಲಿ ತಲಾ ಒಂದರಂತೆ ವೈದ್ಯಕೀಯ ಆಸ್ಪತ್ರೆ-ಕಾಲೇಜುಗಳನ್ನು ತೆರೆಯುವ ಅವಕಾಶವೂ ಇದೆ.</p>
