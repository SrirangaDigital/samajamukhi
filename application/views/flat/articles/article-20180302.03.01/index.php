			<h3>ದೀರ್ಘಾವಧಿ ಬಂಡವಾಳ ವೃದ್ಧಿ ತೆರಿಗೆ</h3>
			<p>ಇದುವರೆಗೆ ನಿಗದಿತ ಶೇರು ಮಾರುಕಟ್ಟೆಗಳ ಮುಖಾಂತರ ಶೇರು ಬಂಡವಾಳ ಹೂಡಿಕೆ ಮಾಡಿ ದೀರ್ಘಾವಧಿ ಲಾಭ ಪಡೆಯುತ್ತಿದ್ದವರು ಯಾವುದೇ ತೆರಿಗೆ ಕಟ್ಟುತ್ತಿರಲಿಲ್ಲ. ಇದರಿಂದ ದೇಶದ ಸಿರಿವಂತರು ದೇಶದ ಶೇರು ಮಾರುಕಟ್ಟೆಯಲ್ಲಿ ಹೆಚ್ಚಿನ ಹೂಡಿಕೆ ಮಾಡುತ್ತಿದ್ದು ಸ್ವಂತ ಉದ್ಯಮಕ್ಕೆ ಉತ್ತೇಜನವಿರಲಿಲ್ಲ. ಇದನ್ನು ಬದಲಿಸುವ ಸಲುವಾಗಿ ಈ ದೀರ್ಘಾವಧಿ ಬಂಡವಾಳ ವೃದ್ಧಿಯ ಮೇಲೆ ಶೇಕಡಾ 10ರ ತೆರಿಗೆ ವಿಧಿಸಲಾಗಿದೆ. ಇದು ಈ ಶೇರು ಮಾರುಕಟ್ಟೆಯಲ್ಲಿ ಹೂಡಿಕೆಯಾದ ಮ್ಯೂಚುಯಲ್ ಫಂಡ್‍ಗಳಿಗೂ ಅನ್ವಯವಾಗುತ್ತದೆ. ಒಂದು ಲಕ್ಷ ರೂಪಾಯಿಗೂ ಮಿಗಿಲಾದ ಈ ಆದಾಯದ ಮೇಲಿನ ತೆರಿಗೆಯಿಂದ ದೇಶದ ತೆರಿಗೆ ಸಂಪನ್ಮೂಲ ಗುರುತರವಾಗಿ ವೃದ್ಧಿಯಾಗಲಿದೆ.</p>
			<p>ಬಹಳಷ್ಟು ವಿಷಯಗಳಲ್ಲಿ ಆಶಾದಾಯಕವಾಗಿರುವ ಈ ಕೇಂದ್ರೀಯ ಮುಂಗಡಪತ್ರದಲ್ಲಿ ಶಿಕ್ಷಣದ ಆಮೂಲಾಗ್ರ ಬದಲಾವಣೆಗೆ ಯಾವುದೇ ಯೋಜನೆಯಿಲ್ಲದಿರುವುದು ಸೋಜಿಗದ ಸಂಗತಿ. ದೇಶದ ಉನ್ನತಶಿಕ್ಷಣಕ್ಕೆ ಕಾಯಕಲ್ಪ ನೀಡಬಲ್ಲ ಹಾಗೂ ಕ್ರಾಂತಿಕಾರಿ ಬದಲಾವಣೆ ತರಬಲ್ಲ ಯೋಜನೆಗಳ ಅಗತ್ಯವನ್ನು ಕೇಂದ್ರ ಸರ್ಕಾರ ಅರಿಯಬೇಕಿದೆ. ಸಂಪನ್ಮೂಲ ಸಂಗ್ರಹ ಹೆಚ್ಚಿಸಲು ವಂಶಪಾರಂಪರ್ಯವಾಗಿ ಬರುವ ಆಸ್ತಿಯ ಮೇಲೆ ತೆರಿಗೆ ವಿಧಿಸಿ ಗಳಿಸಿದ್ದೆಲ್ಲವನ್ನೂ ತಮ್ಮ ಮಕ್ಕಳಿಗೇ ಬಿಟ್ಟು ಹೋಗಬೇಕೆನ್ನುವ ಸ್ವಾರ್ಥ ಮನೋಭಾವಕ್ಕೆ ತುಸುವಾದರೂ ಕಡಿವಾಣ ಹಾಕಬೇಕಾಗಿದೆ. ಅಮೆರಿಕದಲ್ಲಿಯೂ ಇರುವ ಈ ಇನ್ಹೆರಿಟೆನ್ಸ್ ತೆರಿಗೆ ಭಾರತದಲ್ಲಿ ಮತ್ತೆ ಬರಬೇಕಾಗಿದೆ. ನರೇಂದ್ರ ಮೋದಿಯವರು ಮುಂದೆ ಎಂದಾದರೂ ಈ ಕಹಿಮದ್ದನ್ನು ದೇಶದ ಆಗರ್ಭ ಸಿರಿವಂತರಿಗೆ ನೀಡುವರೋ ಎಂದು ಕಾದುನೋಡಬೇಕಾಗಿದೆ.</p>
