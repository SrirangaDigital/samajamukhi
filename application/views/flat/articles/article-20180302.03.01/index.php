			<h1 class="title">ಅಗ್ಗದ ಜನಪ್ರಿಯತೆಯ ಬೆನ್ನು ಬೀಳದ ಕೇಂದ್ರ ಬಜೆಟ್</h1>
			<p>ಮುಂಬರುವ ಲೋಕಸಭಾ ಚುನಾವಣೆಗೂ ಮುನ್ನ ಕೇಂದ್ರ ಸರ್ಕಾರ ಮಂಡಿಸುವ ಆಯವ್ಯಯ ಪತ್ರದಲ್ಲಿ ಜನಪ್ರಿಯ ಘೋಷಣೆಗಳು ಹಾಗೂ ಅನುದಾನಗಳನ್ನು ಅಪೇಕ್ಷಿಸಲಾಗಿತ್ತು. ಚುನಾವಣೆಯಲ್ಲಿ ಮತದಾರರನ್ನು ಓಲೈಸಲು ಸಾಲಮನ್ನಾ, ಬಡ್ಡಿಮನ್ನಾ, ಸಬ್ಸಿಡಿ ಹೆಚ್ಚಳ ಮತ್ತು ತೆರಿಗೆ ಕಡಿತದಂತಹ ಕ್ರಮಗಳನ್ನು ಅಂದಾಜಿಸಲಾಗಿತ್ತು. ಆದರೆ ಇದಕ್ಕೆ ವ್ಯತಿರಿಕ್ತವಾಗಿ ತೆರಿಗೆ ಕಡಿತವಿಲ್ಲದ ಹಾಗೂ ದುಂದುವೆಚ್ಚವಿಲ್ಲದ ಸಮತೋಲಿತ ಆಯವ್ಯಯಪತ್ರವನ್ನು ಹಣಕಾಸು ಮಂತ್ರಿ ಅರುಣ ಜೇಟ್ಲಿ ಮಂಡಿಸಿದ್ದಾರೆ. ಈ ಪತ್ರದಲ್ಲಿ ಕೇಂದ್ರಸರ್ಕಾರ ಹಾಗೂ ಪ್ರಧಾನಮಂತ್ರಿ ಕಾರ್ಯಾಲಯದ ದಿಟ್ಟತನದ ಛಾಪು ಎದ್ದು ಕಾಣುತ್ತಿದೆ.</p>
			<p>24.42 ಲಕ್ಷಕೋಟಿ ರೂಗಳ ಆಯವ್ಯಯದ ಹಾಗೂ ಶೇಕಡಾ 3.3 ವಿತ್ತೀಯ ಕೊರತೆಯ 2018-19ರ ಮುಂಗಡಪತ್ರವನ್ನು ಜೇಟ್ಲಿ ಮಂಡಿಸಿದ್ದಾರೆ. ಕೇಂದ್ರಸರ್ಕಾರದ ತೆರಿಗೆ ಆದಾಯದಲ್ಲಿ ರಾಜ್ಯಗಳ ಪಾಲನ್ನು ಶೇಕಡಾ 32ರಿಂದ 42ಕ್ಕೆ ಏರಿಸಿದ ನಂತರ ಕೇಂದ್ರ ಸರ್ಕಾರದ ಶೇಕಡಾವಾರು ಆದಾಯ ಕಡಿಮೆಯಾಗಿದ್ದರೂ, ಈ ಬಾರಿಯ ಮುಂಗಡಪತ್ರದಲ್ಲಿ ಕಳೆದ ಬಾರಿಯ ರಾಜಸ್ವ ಸಂಗ್ರಹಕ್ಕಿಂತ ರೂ.2.20 ಲಕ್ಷ ಕೋಟಿಗಳ ವೃದ್ಧಿ ಆಗಿದೆ. ಅರ್ಥವ್ಯವಸ್ಥೆಯನ್ನು ಗಲಿಬಿಲಿಗೊಳಿಸಿದ್ದ ನಗದು ಅಮಾನ್ಯೀಕರಣ ಹಾಗೂ ಆರ್ಥಿಕ ಶಿಸ್ತು ತರುವ ನಿಟ್ಟಿನಲ್ಲಿ ಜಿಎಸ್‍ಟಿ ಕಾನೂನು ಜಾರಿಯಾದ ನಂತರದ ಈ ಮುಂಗಡಪತ್ರದ ಅಂಕಿಅಂಶಗಳು ಬಹುಮುಖ್ಯವಾಗಿದ್ದವು. ಕೇಂದ್ರೀಯ ತೆರಿಗೆ ಸಂಗ್ರಹ ಕಡಿಮೆಯಾಗಬಹುದೆಂಬ ಹೆದರಿಕೆಯೂ ಇತ್ತು. 2017-18ರಲ್ಲಿನ ಆರ್ಥಿಕ ಪ್ರಗತಿ 6.75%ಕ್ಕೆ ಇಳಿದ ಮೇಲೂ ತೆರಿಗೆ ಸಂಗ್ರಹದಲ್ಲಿನ ದಾಖಲೆ ಹೆಚ್ಚಳ ಹಾಗೂ ವಿತ್ತೀಯ ಕೊರತೆಯ ಅಪೇಕ್ಷಣೀಯ ಸಂಭವನೀಯತೆ ನಿಜಕ್ಕೂ ಶ್ಲಾಘನೀಯ.</p>
			<p>ಇದರ ಜೊತೆಗೆ ಈ ಮುಂಗಡಪತ್ರದಲ್ಲಿ ಸಾಮಾಜಿಕ ವಲಯದಲ್ಲಿ ಆಮೂಲಾಗ್ರ ಬದಲಾವಣೆ ಬಯಸುವ ಮಹತ್ವಾಕಾಂಕ್ಷೆಯೂ ಇದೆ. ಸದ್ಯಕ್ಕೆ ಕೃಷಿ ಹಾಗೂ ಆರೋಗ್ಯಕ್ಷೇತ್ರಗಳಲ್ಲಿ ಇಟ್ಟ ದಿಟ್ಟ ಹೆಜ್ಜೆಗಳಿವೆ. ಮಧ್ಯಮ ವರ್ಗೀಯರ ಹಾಗೂ ವಿಶೇಷತಃ ಹಿರಿಯ ನಾಗರಿಕರ ಊರುಗೋಲಾಗುವ ಸ್ಪಂದನಶೀಲತೆಯಿದೆ. ದೇಶದ ಷೇರು ಮಾರುಕಟ್ಟೆಯಲ್ಲಿ ಹಣಗಳಿಸಿದವರಿಂದ ತುಸು ಹೆಚ್ಚು ತೆರಿಗೆ ಸಂಗ್ರಹದ ಅಪೇಕ್ಷೆಯಿದೆ. ಉದ್ಯೋಗ ಸೃಷ್ಟಿಗಾಗಿ ಖಾಸಗಿ ವಲಯದಲ್ಲಿ ಬಂಡವಾಳ ಹೂಡಿಕೆಗೆ ಪೂರಕ ಉತ್ತೇಜನಗಳಿವೆ. ಬೆಂಗಳೂರಿಗೊಂದು ಸಬರ್ಬನ್ ರೈಲ್ವೆ ಜಾಲ ನೀಡುವ ಯೋಜನೆಯಿದೆ.</p>
			<h3>ಕೃಷಿ ಬೆಂಬಲ ಬೆಲೆಗೆ ಹೊಸ ಸೂತ್ರ</h3>
			<p>ದೇಶಾದ್ಯಂತ ರೈತರು ಬೆಳೆಯುವ ಉತ್ಪಾದನೆಗೆ ಕನಿಷ್ಠ ಬೆಂಬಲ ಬೆಲೆ ನೀಡಲು ಕೇಂದ್ರ ಸರ್ಕಾರ ಹೊಸ ಫಾರ್ಮುಲ ಘೋಸಿಸಿದೆ. ಒಟ್ಟು ಉತ್ಪಾದನೆಯ ಖರ್ಚಿನ ಶೇಕಡಾ 150ರಷ್ಟನ್ನು ಕನಿಷ್ಠ ಬೆಂಬಲ ಬೆಲೆಯಾಗಿ ನಿಗದಿ ಮಾಡುವ ನಿರ್ಧಾರಕ್ಕೆ ಬರಲಾಗಿದೆ. ಇದರಿಂದ ರೈತರಿಗೆ ಸಿಗುವ ಬೆಂಬಲ ಬೆಲೆ ವೈಜ್ಞಾನಿಕವಾಗಿ ಹಾಗೂ ಸಮಕಾಲೀನವಾಗಿ ನಿರ್ಧಾರವಾಗಲಿದೆ.</p>
			<p>ಕೃಷಿ ಮಾರುಕಟ್ಟೆ ಮೂಲಸೌಕರ್ಯ ನಿಧಿಗೆ ರೂ. 2,000 ಕೋಟಿ ನೀಡಿ 585 ಕೃಷಿ ಉತ್ಪನ್ನ ಮಾರುಕಟ್ಟೆ ಸಮಿತಿ (ಎಪಿಎಂಸಿ) ಹಾಗೂ 22,000 ಗ್ರಾಮೀಣ ಕೃಷಿ ಮಾರುಕಟ್ಟೆ (ಗ್ರಾಮ್)ಗಳಿಗೆ ಅಭಿವೃದ್ಧಿ ಬೆಂಬಲ ನೀಡಲಾಗಿದೆ.</p>
			<p>ಕೃಷಿ ಉತ್ಪನ್ನ ಸಂಸ್ಕರಣಾ ಸಚಿವಾಲಯಕ್ಕೆ 715 ಕೋಟಿ ರೂಗಳ ಬದಲು ರೂ 1,400 ಕೋಟಿ ಅನುದಾನ ನೀಡಿದರೆ ಕೃಷಿ ಸಂಬಂಧಿತ ಸಾಲ ನೀಡಿಕೆಗೆ ರೂ 11 ಲಕ್ಷ ಕೋಟಿ ನಿಗದಿಯಾಗಿದೆ.</p>
