				<h1>ಜನಮಾನಸ ತಲುಪಲಿ</h1>
				<p>‘ಸಮಾಜಮುಖಿ’ ಹೆಸರಿಗೆ ತಕ್ಕಂತೆ ಮೌಲಿಕವಾಗಿ ಬರುವ ಗುಣಮಟ್ಟದ ಪತ್ರಿಕೆಗಳ ಸಾಲಿಗೆ ಸೇರಿದೆ. ಇದು ಜನಮಾನಸಕ್ಕೆ ತಲುಪಿ ಬದಲಾವಣೆ ತರಲಿ - ಯಶಸ್ವಿಯಾಗಿ ಉಳಿಯಲಿ ಎಂದು ಶುಭ ಹಾರೈಸುತ್ತೇನೆ. ನನ್ನ ಬೆಂಬಲವಾಗಿ ಐದು ವರ್ಷಗಳ ಚಂದಾ ಎರಡು ಸಾವಿರ ರೂಗಳನ್ನು ಎಂ.ಓ. ಮೂಲಕ ಈ ದಿನವೇ ಕಳುಹಿಸುತ್ತಿದ್ದೇನೆ.</p>
				<h4>ಜ್ಯೋತಿ ಗುರುಪ್ರಸಾದ್, ಕಾರ್ಕಳ.</h4>
