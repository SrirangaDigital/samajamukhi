				<h1>ಕುತೂಹಲಕರ</h1>
				<p>ಏಪ್ರಿಲ್ ತಿಂಗಳ ಸಂಚಿಕೆಯಲ್ಲಿ ಹಲ್ಮಿಡಿ (ಕ್ರಿ.ಶ.450) ಶಾಸನಕ್ಕಿಂತ ಹಿಂದಿನ ಶಾಸನ (ಕ್ರಿ.ಶ.370) ತಾಳಗುಂದದಲ್ಲಿ ಪತ್ತೆಯಾಗಿದೆ ಎಂಬ ಕುತೂಹಲಕರ ಸುದ್ದಿಯನ್ನು ಪ್ರಕಟಿಸಿದ್ದೀರಿ, ಧನ್ಯವಾದಗಳು. ದಯವಿಟ್ಟು ಪೂರ್ಣ ವಿವರವನ್ನು ಬರುವ ಸಂಚಿಕೆಯಲ್ಲಿ ಪ್ರಕಟಿಸಿ ಉಪಕರಿಸುವಿರಾ!</p>
