				<h1>ಕಹಿ ಅನುಭವ</h1>
				<p>ಸದ್ಯ ಒಂದು ವರ್ಷದ ಚಂದಾ ಕಳಿಸುತ್ತಿರುವೆ. ಈ ಬಗೆಯ ಪತ್ರಿಕೆಗಳು ಅಕಾಲ ಮರಣಕ್ಕೆ ತುತ್ತಾದ ಕಹಿ ಅನುಭವ ನನಗಿದೆ. ಪತ್ರಿಕೆಯ ಚಂದಾ ಹಣವನ್ನು ಕಡಿಮೆ ಮಾಡಬಹುದಿತ್ತು -ಬಳಸಿದ ಕಾಗದ ಇತ್ಯಾದಿಗಳ ಗುಣಮಟ್ಟವನ್ನು ಕೊಂಚ ಕಡಿಮೆ ಮಾಡುವ ಮೂಲಕ. ಲೇಖನಗಳು ಚೆನ್ನಾಗಿವೆ.</p>
