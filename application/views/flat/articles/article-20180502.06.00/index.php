				<h1>ಅದೊಂದೇ ಪರ್ಯಾಯ</h1>
				<p>‘ಹಾಳೆ ಹಳೆಯದಾದರೇನು...’ ಓದಿದೆ. ಪರ್ಯಾಯ ರಾಜಕೀಯ ಕುರಿತು ಬರೆಯುತ್ತಾ ಇದು ಸಾಧ್ಯವಾಗುವುದು ನೈತಿಕ ನೆಲೆಯ ಪ್ರತಿ ರಾಜಕಾರಣದಿಂದ ಮಾತ್ರ ಎನ್ನಲಾಗಿದೆ. ನಿಜ, ಅದು ಇಂದು ಕಂಡು ಬರುತ್ತಿರುವುದು ಕಮ್ಯುನಿಸ್ಟ್ ಪಕ್ಷದಲ್ಲಿ ಮಾತ್ರ. ಒಂದು ಮನೆಯನ್ನೇ ನಿಭಾಯಿಸಲು ಆರ್ಥಿಕ ತಜ್ಞತೆ ಬೇಕಿರುವಾಗ ರಾಜಕೀಯ ಪಕ್ಷಗಳಿಗೆ ಅಂಥ ಆರ್ಥಿಕ ತಜ್ಞತೆ ಇರದಿದ್ದರೆ ಹೇಗೆ? ಕಾಂಗ್ರೆಸ್, ಬಿಜೆಪಿ, ಜೆಡಿಎಸ್ ಮುಂತಾದ ಪಕ್ಷಗಳು ಅಧಿಕಾರ ನಡೆಸಿದರೂ ನಮ್ಮ ಸ್ಥಿತಿ ಹೀಗಿರಲು ಈ ಎಲ್ಲಾ ಪಕ್ಷಗಳ ಬೂಜ್ರ್ವಾ ರೀತಿನೀತಿಗಳೇ ಕಾರಣವಾಗಿವೆ. ಸಾವಿರಾರು ವರ್ಷಗಳಿಂದ ನರಳುತ್ತಿರುವ ಮನುಕುಲಕ್ಕೆ ಆ ಎಲ್ಲಾ ಐತಿಹಾಸಿಕ ಅರಿವಿರುವ ಆರ್ಥಿಕ ಸಿದ್ಧಾಂತವನ್ನು ಹೊಂದಿರುವ ಕಮ್ಯುನಿಸ್ಟ್ ಪಕ್ಷಗಳಿಂದ ಮಾತ್ರ ಪರ್ಯಾಯ ರಾಜಕಾರಣವನ್ನು ನಿರೀಕ್ಷಿಸಬಹುದು. ಹೀಗೆ ಹೇಳುವಾಗಲೂ ಕಮ್ಯುನಿಸ್ಟ್ ಸರಕಾರಗಳಿಗೆ ಕೂಡ ಕೆಲವು ಇತಿಮಿತಿಗಳಿರುತ್ತವೆಂದು ನಾನು ಬಲ್ಲೆ. ಆದರೆ ಸದ್ಯಕ್ಕೆ ಅದೊಂದೇ ಪರ್ಯಾಯ ಎಂಬುದು ನನ್ನ ಕಾಣ್ಕೆ.</p>
				<h4>ಹುರುಕಡ್ಲಿ ಶಿವಕುಮಾರ, ಬಾಚಿಗೊಂಡನಹಳ್ಳಿ.</h4>
