				<p>ನಾನು ಕೆಲವು ಕಾಲ ಅಮೆರಿಕಾಕ್ಕೆ ಹೋಗಿದ್ದೆ. ಬಂದ ನಂತರ ಸಂಚಿಕೆಗಳನ್ನು ಗಮನಿಸಿದ್ದೇನೆ. ಲಂಕೇಶ್ ಪತ್ರಿಕೆಯ ನಂತರ ಸಮಾಜ, ರಾಜಕಾರಣ ಮತ್ತು ಸಾಹಿತ್ಯ-ಸಂಸ್ಕೃತಿಯನ್ನು ಗಂಭೀರವಾಗಿ ವಿಶ್ಲೇಷಿಸುವ ಇಂಥ ಪತ್ರಿಕೆಯ ಅಗತ್ಯವಿತ್ತು. ಬಹು ಕಾಲದ ಆ ಕೊರತೆಯನ್ನು ಸಮಾಜಮುಖಿ ತುಂಬಬಲ್ಲದು ಎಂಬುದು ನನ್ನ ನಂಬಿಕೆ. ನನಗೆ ಪತ್ರಿಕೆ ಕಳಿಸಿ, ಚಂದಾ ಕಳಿಸುತ್ತೇನೆ.</p>
