				<h1>ಮೂಲರಾಮಾಯಣ?!</h1>
				<p>ಮಾರ್ಚ್ ತಿಂಗಳ ಸಂಚಿಕೆ. ಉತ್ತರಕಾಂಡ ಕಾದಂಬರಿ ಕುರಿತ ಜಿ.ಬಿ.ಹರೀಶ್ ಲೇಖನ. ‘ಮೂಲರಾಮಾಯಣ’ ಎಂಬ ಮಾತು. ‘ಮೂಲ’ ರಾಮಾಯಣ ಅಂದರೇನು?! ಬೇಕಿದ್ದರೆ, ‘ವಾಲ್ಮೀಕಿ’ ರಾಮಾಯಣ ಅನ್ನಲಿ. ಆ ತಥಾಕಥಿತ ವಾಲ್ಮೀಕಿ ಎಲ್ಲೆಲ್ಲಿಂದಲೂ ತನ್ನ ಕಥಾವಸ್ತು ಮತ್ತು ವಿವರ ಪಡೆದನಲ್ಲವೆ? ಅಥವಾ ವಾಲ್ಮೀಕಿಯರು ‘ಪಡೆದರು’ ಅನ್ನಬೇಕೇನೋ. ಅದೊಂದು ಪರಂಪರೆ...</p>
				<h4>ರಘುನಂದನ, ಬೆಂಗಳೂರು.</h4>
