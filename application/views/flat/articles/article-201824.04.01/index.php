			<p>‘‘तिष्ठ !... किं.. किमुक्तं त्वया ? त्वं मां समये प्रतिगमयन् असि किम् ?’’ - त्वम् आश्चर्येण स्तब्धः सन् अपृच्छः । </p>
			<p>‘‘वस्तुनिष्ठकल्पनां कुरु तावत् । समयः युष्माकं विश्वे एव अस्तित्वं वहति । यतः मया आगतं, तत्र वस्तुस्थितिः भिन्ना एव वर्तते ।’’</p>
