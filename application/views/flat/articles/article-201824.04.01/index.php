			<p>‘‘आम्, अहं देवः अस्मि’’ - अहम् अवोचम् ।</p>
			<p> त्वम् अवदः साशङ्कम् - ‘‘मम पुत्राः, मम पत्नी.... । तेषां का गतिः?  ते आनन्देन जीवेयुः किम् ?’’</p>
			<p>मया प्रहसता उक्तम् - ‘‘एतत् अवधानयोग्यमस्ति । त्वम् इदमिदानीं मृतः । अतः एव ते कुटुम्बस्य चिन्ता महत्त्वं वहति । विलक्षणम् एतत् !’’</p>
			<p>त्वं चकितः सन् माम् अपश्यः । त्वया मयि देवत्व-लक्षणं किमपि न दृष्टम् । कञ्चित् अज्ञातं पुरुषम् इव माम् अपश्यः । अथवा स्त्रियम् इव अपि स्यात् । कञ्चित् न्यायदातारम् इव अपि स्यात् । सर्वशक्तत्वेन न, अपि तु कस्याश्चित् शालायाः व्याकरणशिक्षकः इव दृष्टः स्याम् अपि । </p>
			<p>अहम् अवोचम् - ‘‘चिन्तां मा कार्षीः । ते समाहिताः भविष्यन्ति । तव पुत्राः त्वां सर्वेषु अपि कार्येषु आदर्शरूपेण स्मरिष्यन्ति । तव तिरस्कारार्हान् गुणान् ज्ञातुं तैः समयः न प्राप्यते । तव पत्नी सर्वेषां पुरतः तु रोदनं करोति, किन्तु तस्याः अन्तःकरणं तव मरणात् आश्वस्तम् अस्ति । वस्तुतः तु तव विवाहजीवनं विच्छिन्नं जायमानम् आसीत् । अतः तव मृतिः तस्यां महत् दुःखं तु न अजनयत् ।...’’</p>
			<p>‘‘एवम् ! अधुना किं भविष्यति ?.... अहं स्वर्गं गमिष्यामि, उत नरकम्, उताहो अन्यत्र क्वापि ?’’</p>
			<p>‘‘उभयत्रापि न । त्वं पुनः जन्म प्राप्स्यसि ।...’’</p>
			<p>‘‘अहो ! एवं तर्हि हिन्दुभिः यत् उक्तं तत् वास्तविकम् एव ! ...’’</p>
			<p>‘‘सर्वेऽपि धर्माः स्वीयया रीत्या समीचीनाः एव । आस्तां तावत्, चल मया सह !’’</p>
			<p>तस्मिन् शून्ये चलन्तं मां त्वम् अन्वसरः । </p>
			<p>‘‘आवां कुत्र गच्छन्तौ स्वः ?’’ - त्वम् अपृच्छः ।</p>
			<p>‘‘विशेषतः न क्वापि । सम्भाषणावसरेे विहारः युज्यते इत्येतदर्थं पदानि स्थाप्यन्ते’’ - मया उत्तरं दत्तम् । </p>
			<p>‘‘यदा अहं पुनर्जन्म प्राप्स्यामि, पुनः शून्यपत्रताम् अवाप्स्यामि ननु ? कश्चन शिशुः । अस्मिन् जन्मनि मया यत् अधीतं, ये अनुभवाः प्राप्ताः, अन्यदपि यत् सर्वम् आसादितं तत् किं सर्वं व्यर्थतां याति ? तेन किं साधितं    भवेत् ?’’</p>
			<p>‘‘तथा न !.... सर्वेषु अपि पूर्वजन्मसु त्वया अर्जितं सर्वमपि ज्ञानम् अनुभवो वा, त्वय्येव निहितं भवति । तस्य सर्वस्य स्मरणम् अधुना न भवति तव, तावदेव’’ - अहम् अवोचम् ।</p>
			<p>मया विहारं स्थगयित्वा तव भुजौ गृहीतौ । </p>
			<p>‘‘त्वं कल्पयितुम् अपि न शक्नोषि ! तव आत्मा भव्यः, अविनाशी, सुन्दरः, अप्रमेयः च ! त्वं यदसि, तस्य अणु-मात्रमेव ग्रहीतुं शक्नोति मनुष्यबुद्धिः । जलम् उष्णम् अस्ति उत शीतलम् इति ज्ञातुं यथा त्वं स्वस्य अङ्गुलीं जलचषके निमज्जयसि, तथा आत्मनः लघुभागं मनुष्यदेहादिषु पात्रेषु स्थापयसि । सः आत्मभागः बहिः यदा आनीयेत, तदा तेन भागेन अनुभूतं सर्वं त्वदीयं भवेत् । गतेभ्यः आ अष्टचत्वारिंशतः वर्षेभ्यः त्वं मनुष्यशरीरे आसीः । अतः इतोऽपि त्वम् आत्मनः महीयस्त्वम्, अखण्डप्रज्ञानं च न प्राप्तवान् । एवंरीत्या आवाम् इतोऽपि किञ्चित् कालं सम्भाषमाणौ यापयेव चेत् त्वं सर्वम् अपि स्मर्तुम् आरभेथाः । किन्तु जन्मनां विषये भेदचिन्तनात् न किमपि प्रयोजनम् ।...’’</p>
			<p>‘‘एवं तर्हि कतिवारं पुनर्जन्म प्राप्तं मया ?!’’</p>
