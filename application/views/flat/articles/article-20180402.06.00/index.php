				<h1>ಧಿಕ್ಕಾರವಿರಲಿ...</h1>
				<p>ಅಂತಾರಾಷ್ಟ್ರೀಯ ಮಹಿಳಾ ದಿನಾಚರಣೆಯ ಸಂಬಂಧ ಬೇರೆಬೇರೆ ಪತ್ರಿಕೆ, ದೂರದರ್ಶನ, ಫೇಸ್‍ಬುಕ್, ವಾಟ್ಸ್ ಆ್ಯಪ್ ಗಳಲ್ಲಿ ಅನೇಕ ಮಹಿಳಾ ಸಾಧಕಿಯರ ಯಶೋಗಾಥೆಗಳನ್ನು, ಸಂಭ್ರಮಾಚರಣೆಗಳನ್ನು ಕಂಡು ಮನಸ್ಸು ಸ್ತ್ರೀಕುಲದ ಬಗ್ಗೆ ಹೆಮ್ಮೆಯಿಂದ ಸಂತಸಗೊಂಡು ಹಕ್ಕಿಯಂತೆ ಹಾರಿ ಸಂಭ್ರಮಿಸುವಾಗ ಥಟ್ಟನೆ ರೆಕ್ಕೆ ಕತ್ತರಿಸಿದಂತಾಗಿ ವಿಲವಿಲ ಒದ್ದಾಡುವಂತೆ ಮಾಡಿದೆ “ಮೊಲೆ ಕತ್ತರಿಸಿಕೊಟ್ಟ ನಂಗೇಲಿ ನೆನಪು” ಲೇಖನ! ಎಂಥಾ ಅಮಾನುಷ ಪದ್ಧತಿ! ಅದೇ ರೀತಿ ಯೋನಿವಿಚ್ಛೇದನ ಕೂಡ! ಧಿಕ್ಕಾರವಿರಲಿ ಇಂಥ ಹೀನ ಆಚರಣೆಗಳಿಗೆ. ಈಗಲೂ ಅತ್ಯಾಚಾರ ತಡೆಯುವ ಸಂಬಂಧವಾಗಿ ‘ಮಹಿಳೆಯರು ಸಬಲರಾಗಬೇಕು’, ‘ಯುವತಿಯರು ಮುಂಜಾಗ್ರತಾ ಕ್ರಮಗಳನ್ನು ಅನುಸರಿಸಬೇಕು’, ಅವರ ಉಡುಗೆತೊಡುಗೆ ಹಾಗಿರಬೇಕು, ಹೀಗಿರಬಾರದು... ಮುಂತಾದ ಉಪದೇಶಗಳು ಮಹಿಳೆಯರತ್ತ ಪುಂಖಾನುಪುಂಖವಾಗಿ ಹರಿದು ಬರುತ್ತಿವೆ. ಪೌಢಶಾಲೆಗಳಲ್ಲಿ ಸ್ವಯಂರಕ್ಷಣಾತಂತ್ರದ ಕರಾಟೆ ತರಬೇತಿಯನ್ನು ಹುಡುಗಿಯರಿಗೆ ಸಂಬಂಧಿಸಿದ ಇಲಾಖೆ ನೀಡುತ್ತಿದೆ. ಆದರೆ ಯುವಕರಿಗೆ ಮಹಿಳೆಯರನ್ನು ಗೌರವಯುತವಾಗಿ ಕಾಣಬೇಕು ಎಂದು ಯಾವ ಜಾಹೀರಾತು, ಯಾವ ನಿರ್ದಿಷ್ಟ ಸರಕಾರಿ ಯೋಜನೆಗಳು, ಕಾರ್ಯಕ್ರಮಗಳು ಅಪೇಕ್ಷಿತ ಪ್ರಮಾಣದಲ್ಲಿ ಕಾಣದಿರುವುದು ವಿಪರ್ಯಸವೇ ಸರಿ.</p>
