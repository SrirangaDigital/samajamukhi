				<p>ಪತ್ರಿಕೆಯ ಅಂದಚೆಂದ ಚೆನ್ನಾಗಿದೆ. ಅಭಿನಂದನೆಗಳು. ಪತ್ರಿಕೆ ಸಮಾಜದಲ್ಲಿ ಜನಪ್ರಿಯವಾಗಲಿ, ಸಮಾಜದ ಸ್ವಾಸ್ಥ್ಯಕ್ಕೆ ದಾರಿಮಾಡಿಕೊಡಲಿ ಎಂದು ಹಾರೈಸುತ್ತೇನೆ. ಇಂದು ಆರೋಗ್ಯವಂತರಿಗಿಂತ ಅನಾರೋಗ್ಯವಂತರೇ ಹೆಚ್ಚಾಗಿದ್ದಾರೆ. ಶೇ.70ರಷ್ಟು ಜನ ದೈಹಿಕವಾಗಿ/ಮಾನಸಿಕವಾಗಿ ಅಸ್ವಸ್ಥರು. ಯಾವ ಸಮಾಜದಲ್ಲಿ ಹೆಚ್ಚಿನ ವ್ಯಕ್ತಿಗಳು ಅಸ್ವಸ್ಥರಿರುತ್ತಾರೋ ಆ ಸಮುದಾಯ, ಸಮಾಜವೂ ಅಸ್ವಸ್ಥವೇ. ಆರೋಗ್ಯ ಭಾಗ್ಯವನ್ನು ವೈದ್ಯರಾಗಲಿ ಸರಕಾರವಾಗಲಿ ಕೊಡಲು ಸಾಧ್ಯವಿಲ್ಲ. ಪ್ರತಿಯೊಬ್ಬ ವ್ಯಕ್ತಿ ಆರೋಗ್ಯವನ್ನು ತಾನೇ ಸಂಪಾದಿಸಬೇಕು.</p>
