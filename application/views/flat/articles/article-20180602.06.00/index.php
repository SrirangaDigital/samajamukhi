			<div id="201806.03">
				<h1>ಯಶಸ್ವಿಯಾಗಲಿ</h1>
				<p>ನನಗೀಗ 82 ವಯಸ್ಸು. ಹೊಸ ಓದನ್ನು ಮನೋಸ್ಥಿತಿ ಸ್ವೀಕರಿಸುತ್ತಿಲ್ಲ. ನಮ್ಮದು ಕೃಷಿಯ ಬದುಕು. ಕುಟುಂಬ, ಕೃಷಿ ಎಲ್ಲಾ ನನ್ನ ಹೊಣೆ. ರೂಢಿ ಬಲದಿಂದ ಒಂದಿಷ್ಟು ಕ್ರಿಯಾಶೀಲನಾಗಿರುವೆ. ಮನೋಬಲದಿಂದ ಚಲನೆ. ಆದರೆ ದೇಹ ಬಲವಿಲ್ಲ. ಒಂದು ವರ್ಷದ ಚಂದಾ ಸದ್ಯ ಕಳಿಸುವೆ. ನಿಮ್ಮ ಪ್ರಯತ್ನ ಯಶಸ್ವಿಯಾಗಲಿ ಎಂದು ಹರಸುವೆ.</p>
				<h4>ನಾ.ಸು.ಭರತನಹಳ್ಳಿ, ಕುಂದರಗಿ.</h4>
			</div>
			<div id="201806.04">
				<h1>ಸಲಾಂ</h1>
				<p>ನಿಜಕ್ಕೂ ಇದೊಂದು ಅದ್ಭುತ ಪ್ರಯತ್ನ. ಪತ್ರಿಕೆ ಮಾಡುವುದೇ ಸಾಹಸದ ಕೆಲಸ. ಅದರಲ್ಲಿ ಖ್ಯಾತನಾಮರ ಲೇಖನಗಳನ್ನು ಸಂಗ್ರಹಿಸಿ ಪ್ರಕಟಿಸುವುದು ಮತ್ತೊಂದು ಸಾಹಸ. ಈ ಕೆಲಸವನ್ನು ಸಮಾಜಮುಖಿ ಯಶಸ್ವಿಯಾಗಿ ಮಾಡುತ್ತಿದೆ. ಅರ್ಥಪೂರ್ಣ ಲೇಖನಗಳ ಮೂಲಕ ಗಮನ ಸೆಳೆಯುತ್ತಿದೆ. ಮಾಹಿತಿಪೂರ್ಣ ಲೇಖನಗಳು ಸಂಗ್ರಹಯೋಗ್ಯವಾಗಿವೆ. ಪತ್ರಿಕೆಗೆ ನಮ್ಮದೊಂದು ಅಭಿಮಾನದ ಸಲಾಂ.</p>
				<h4>ಕೆ.ಎಸ್.ಭುವನ್ ಗೌಡ, ಶಿರಾಳಕೊಪ್ಪ.</h4>
			</div>
			<div id="201806.05">
				<h1>ಅನಿವಾರ್ಯ</h1>
				<p>ಅನ್ನದ ಭಾಷೆಯ ಚಿನ್ನದ ಮಾಸಿಕ ಘೋಷವಾಕ್ಯ ಹೊತ್ತ ಚಿಂತನಶೀಲ ಪತ್ರಿಕೆ ತಲುಪಿದೆ ಸದ್ಯದ ಪರಿಸ್ಥಿತಿಯಲ್ಲಿ ಇಂತಹ ಪತ್ರಿಕೆ ಅವಶ್ಯಕತೆ ಅಷ್ಟೆ ಅಲ್ಲ, ಅನಿವಾರ್ಯತೆ ಇದೆ.</p>
				<h4>ಅಜಮೀರ ನಂದಾಪುರ, ಗಂಗಾವತಿ.</h4>
