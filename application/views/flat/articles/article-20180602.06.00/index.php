				<h1>ಕ್ಯಾರೆಟ್ ತಗ್ಗಿಸಿ!</h1>
				<p>ಪತ್ರಿಕೆಯನ್ನು ಅಭಿಮಾನದಿಂದ ಕಳಿಸಿ ಓದಲು ಹಚ್ಚಿದ್ದಕ್ಕೆ ನಾನು ಚಿರಋಣಿ. ಇದರೊಂದಿಗೆ ಐದು ವರ್ಷಗಳ ಚಂದಾ ಹಣ 2000 ರೂ. ಚೆಕ್ ಇದೆ. ನಿಮ್ಮ ಪ್ರಯತ್ನದ ಸಮಾಜಮುಖಿ ಯಶಸ್ವಿಯಾಗಲಿ. ನಾನು ಕೂಡಾ ಚಂದಾದಾರರಾಗಲು ಕೆಲವರಿಗೆ ಸೂಚಿಸುತ್ತೇನೆ. ಪತ್ರಿಕೆ 24 ಕ್ಯಾರೆಟ್ ಬಂಗಾರದಂತಿದೆ. ಅದನ್ನು ಕರಗಿಸಿ ಜನಪ್ರಿಯ ಸಾಹಿತ್ಯ, ಸಿನಿಮಾ ಸೇರಿಸಿ ಹದಗೊಳಿಸಿ. ಬೌದ್ಧಿಕ ಮಟ್ಟ ಸರಿಸಮಾನವಾಗಿರಲಿ.</p>
				<h4>ರಮೇಶ ಸುರ್ವೆ, ಬೆಂಗಳೂರು.</h4>
			</div>
			<div id="201806.02">
				<h1>ಎಲ್ಲರಿಗೂ ಇಷ್ಟ</h1>
				<p>ಉತ್ತಮ ಚಿಂತನಶೀಲ ಬರಹಗಳನ್ನು ಒಳಗೊಂಡ ಪತ್ರಿಕೆ ಉತ್ತಮ ಗುಣಮಟ್ಟದಿಂದ ಕೂಡಿದೆ. ಕಾಗದ, ಮುದ್ರಣ ಅತ್ಯುತ್ತಮವಾಗಿದೆ. ವೈವಿಧ್ಯಮಯ ವಿಷಯಗಳನ್ನೊಳಗೊಂಡು ಎಲ್ಲಾ ವಯೋಮಾನದವರಿಗೂ ಇಷ್ಟವಾಗುವಂತಿದೆ. ಹನಿಗವನ, ಚುಟುಕ, ಕವನಗಳಿಗೆ ಅವಕಾಶ ಮಾಡಿಕೊಡಿ. ರಾಜಕೀಯ ವಿಷಯಗಳು ಕಡಿಮೆಯಾದರೂ ವಿಜ್ಞಾನದ ವಿಷಯಗಳಿಗೆ ಆದ್ಯತೆ ನೀಡಿ. ಕ್ರಿಕೆಟ್, ಸಿನಿಮಾಗಳ ಬಗ್ಗೆ ಹೆಚ್ಚು ಪುಟಗಳನ್ನು ಮೀಸಲಿರಿಸಬೇಡಿ.</p>
				<h4>ಎಸ್.ಓಂಕಾರಯ್ಯ ತವನಿಧಿ, ದಾವಣಗೆರೆ.</h4>
