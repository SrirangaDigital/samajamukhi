			<div id="201806.06">
				<h1>ಇದು ಪ್ರತ್ಯೇಕ</h1>
				<p>ಆತಂಕದ ಕಾಲಘಟ್ಟದಲ್ಲಿ “ಸಮಾಜಮುಖಿ” ಆಶಾದಾಯಕ ಪ್ರಯತ್ನ ಆಗಿದೆ. ಪ್ರಚಲಿತ ವಿದ್ಯಮಾನಗಳಿಗೆ ಯಾವುದೇ ‘ಭೂತಗನ್ನಡಿ’ ಹಿಡಿಯದೇ ನೈಜ ಪ್ರತಿಫಲನಕ್ಕೆ ಒತ್ತು ನೀಡಿ ಚಲನಶೀಲ ಹಾಗೂ ಚಿಂತನಶೀಲ ಸಮಾಜದ ಪುನರುತ್ಥಾನಕ್ಕೆ ಸಮಾಜಮುಖಿ ಪತ್ರಿಕೆ ಪ್ರಾಮಾಣಿಕ ವೇದಿಕೆ ಆಗಿದೆ. ವಿದ್ಯುನ್ಮಾನ ಮಾಧ್ಯಮ ಸೇರಿ ಆಧುನಿಕ ಮಾಧ್ಯಮಗಳು ತುಳಿಯುತ್ತಿರುವ ಅವಸರ, ಅರೆಬೆಂದ ಹಾಗೂ ಅವಿಶ್ವಾಸರ್ಹ ಸುದ್ದಿಗಳಿಂದ ಸಮಾಜಮುಖಿ ಪತ್ರಿಕೆ ಪ್ರತ್ಯೇಕತೆ ಕಾಪಾಡಿಕೊಂಡಿರುವುದು ಗಮನಾರ್ಹ.</p>
				<h4>ಹನುಮೇಶ ಯಾವಗಲ್ಲ, ಬೆಂಗಳೂರು.</h4>
