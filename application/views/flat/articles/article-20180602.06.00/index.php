				<p>ಹತ್ತು ರೂಪಾಯಿ ಅಂಚೆ ಖರ್ಚು! ಕೂಡಲೇ ರಿಜಿಸ್ಟ್ರಾರ್ ನ್ಯೂಸ್ ಪೇಪರ್ ಅನುಮತಿ ಪತ್ರ ಪಡೆದು ಕರ್ನಾಟಕ ಅಂಚೆ ಸೇವೆಗಳ ಪ್ರಧಾನ ಅಧೀಕ್ಷಕರಿಗೆ ಕೊಟ್ಟು ಅಂಚೆ ಸೇವೆ ಪಡೆಯಿರಿ. ಒಂದೇ ರೂಪಾಯಿಗೆ ರವಾನೆ ಮಾಡಬಲ್ಲಿರಿ. ಮಾಡಿರಿ, ಮಾಡಿರಿ, ಮಾಡಿರಿ. ಕರ್ನಾಟಕದ ಎಲ್ಲ ವಿಳಾಸಗಳ ಭಾಷೆ ಕನ್ನಡವೇ ಇರಲಿ. ಪಿನ್ ಇಂಗ್ಲಿಷ್ ಅಂಕಿಯಲ್ಲಿರಲಿ. ಕನ್ನಡವನ್ನು ಮಾತಿನಲ್ಲಿ, ಬರಹದಲ್ಲಿ, ವ್ಯವಹಾರದಲ್ಲಿ, ಮಾರುಕಟ್ಟೆಯಲ್ಲಿ ಬಳಸುವ ಮೂಲಕವೇ ಉಳಿಸಿಕೊಳ್ಳಬೇಕಾಗಿದೆ.</p>
