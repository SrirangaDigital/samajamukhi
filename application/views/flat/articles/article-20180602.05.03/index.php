			<h3>ನೆಳಲು ಬೆಳಕಿನ ಆಟ</h3>
			<p>ಸ್ಥಿರ ಚಿತ್ರ ಛಾಯಾಗ್ರಹಣ ಇಂದು ಜನಪ್ರಿಯ ಮಾಧ್ಯಮವಾಗಿಲ್ಲ . ಸಾಮಾನ್ಯವಾಗಿ ಇಂದು ಕ್ಯಾಮೆರಾ ಇಟ್ಟುಕೊಂಡಿರುವ ಎಲ್ಲರೂ ವನ್ಯ ಜೀವಿಗಳ ಚಿತ್ರವನ್ನು ಸೆರೆ ಹಿಡಿಯಲು ಬಯಸುತ್ತಾರೆ; ಬಹುಶಃ ಅವುಗಳ ಬಣ್ಣ ಕೋಮಲತೆ ಜೀವಂತಿಕೆಯ ಚಲನೆ ಕಾರಣವಿರಬಹುದು. ಏನೇ ಇರಲಿ, ಈ ಸ್ಥಿರ ಚಿತ್ರದಲ್ಲಿ ಛಾಯಾಗ್ರಹಣದ ಕೌಶಲ್ಯ ಎತ್ತಿ ಹಿಡಿಯುವ ಅನೇಕ ಸಂಗತಿಗಳು ಮೇಳೈಸಿವೆ. ಗಾಜಿನ ಲೋಟವನ್ನು ಬಣ್ಣದಲ್ಲಿ ಕಲಾಕೃತಿಯನ್ನಾಗಿಸಿ, ಆ ಕಲಾಕೃತಿಯನ್ನೇ ಛಾಯಾಗ್ರಹಣದೊಂದಿಗೆ ಮತ್ತೆ ಕಲಾಕೃತಿಯನ್ನಾಗಿಸುವುದು ಸವಾಲಿನ ಸಂಗತಿ. ಇಲ್ಲಿ ಬೆಳಕು ಮತ್ತು ಕತ್ತಲನ್ನು ಹದವಾಗಿ ಬೆರೆಸುವುದರೊಂದಿಗೆ ಮುಖ್ಯ ವಸ್ತುವಿನ ಬಣ್ಣಗಳು ಹಾಗೂ ಅದರ ನೆರಳು ಕೂಡ ಕಲೆಯಾಗಿ ಗಮನ ಸೆಳೆಯುತ್ತಿವೆ. ಮೇಲಿನಿಂದ ಬಿದ್ದ ಬೆಳಕು ಪ್ರತಿಫಲಿಸಿ ಲೋಟದ ಕಂಠದಲ್ಲಿ ಬೇರೆಯದೇ ಆದ ಮಹತ್ವದ ಆಯಾಮವನ್ನು ನೀಡುತ್ತಿದೆ. ಮುಖ್ಯವಸ್ತು ಬೆಳಕಿನಲ್ಲಿದ್ದು ಸುತ್ತಲೂ ಕತ್ತಲು ತಬ್ಬಿದೆ. ಈ ನೆಳಲು ಬೆಳಕಿನಾಟವೇ ಉತ್ತಮ ಸ್ಥಿರ ಛಾಯಾಗ್ರಹಣಕ್ಕೆ ಸಾಕ್ಷಿಯಾದಂತೆ ಈ ಚಿತ್ರವಿದೆ.</p>
