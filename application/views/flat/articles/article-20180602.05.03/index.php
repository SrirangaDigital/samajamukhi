			<h3>ಮುಚ್ಚಿದ ಬಾಗಿಲು</h3>
			<p>ಈ ಚಿತ್ರ ತೆಗೆದದ್ದು ಹಾವೇರಿ ಜಿಲ್ಲೆ ಕರಜಗಿ ಗ್ರಾಮದಲ್ಲಿ. ಈ ಮನೆ ಊರಿನ ಗಲಗಲಿ ಕುಟುಂಬಕ್ಕೆ ಸೇರಿದ್ದು. ಈ ಭಾಗದ ವಿಶಿಷ್ಟ ಕಟ್ಟಡ ಸಾಮಗ್ರಿ ಹಾಗೂ ರಚನಾ ಕ್ರಮವನ್ನು ಇಲ್ಲಿ ಕಾಣಬಹುದು. ವಿವಿಧ ಅಳತೆ ಆಕಾರದ ಚಪ್ಪಟೆ ಕಲ್ಲುಗಳನ್ನು ಅತ್ಯಂತ ಲೆಕ್ಕಾಚಾರದಿಂದ ಜೋಡಿಸಿ ನಿರ್ಮಿಸಿದ ಇಲ್ಲಿನ ಮನೆಗಳ ಸ್ಥಳೀಯ ಶೈಲಿ ಗಮನಾರ್ಹ. ಕಟ್ಟಿಗೆಯ ತಲಬಾಗಿಲು, ವಿಶಾಲ ಜಗಲಿಕಟ್ಟೆ, ಆನಿಸಿ ನಿಲ್ಲಿಸಿದ ಸೈಕಲ್ಲು, ಸೈಜುಗಲ್ಲಿನ ಮೆಟ್ಟಿಲು ಸೂಜಿಗಲ್ಲಿನಂತೆ ಸೆಳೆಯುತ್ತವೆ. ತೆರೆದ ಕಿಟಕಿಯೊಳಗೆ ಇಣಿಕಿದರೆ ಗತಕಾಲದ ನೆನಪುಗಳ ನೋಟ. ಕೊನೆಗೂ ಮುಚ್ಚಿದ ಬಾಗಿಲು ಮಾತ್ರ ಭದ್ರ; ಬದುಕು ಶಿಥಿಲ!</p>
